"""
se piden dos numeros y se hacen todas las op basicas con dicho numero
"""
print("\n\nVamos a hallar todas las operaciones basicas que se puedan hallar con 2 numeros.\n\n")
n1=int(input("Ingrese el primer numero: "))
n2=int(input("Ingrese el segundo numero: "))

suma=n1+n2
resta=n1-n2
multiplicacion=n1*n2
division=n1/n2

print(f"SUMA: {n1}+{n2}={suma}\nRESTA: {n1}-{n2}={resta}\nMULTIPLICACION: {n1}*{n2}={multiplicacion} \nDIVISION: {n1}/{n2}={division}")

